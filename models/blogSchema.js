//Schema for blog collections in our model
const mongoose = require("mongoose");
const Schema = mongoose.Schema; // this is  a constructor function

const blogSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    snippet: {
      type: String,
      required: true,
    },
    body: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = blogSchema;
